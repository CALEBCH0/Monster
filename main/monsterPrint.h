#ifndef MONSTERPRINT_H_
#define MONSTERPRINT_H_

#include <iostream>

void printSlime(){
    std::cout << " _____  \n";
    std::cout << "( * * ) \n";
    std::cout << " TTTTT  \n";
}

void printSpider(){
    std::cout << "            \n";
    std::cout << " /\\_____/\\  \n";
    std::cout << "/ ( * * ) \\ \n";
}

void printOrc(){
    std::cout << " \n";
    std::cout << "  (**)    \n";
    std::cout << " /|__|\\  \n";
    std::cout << "  /  \\   \n";
}

void printOgre(){
    std::cout << "            \n";
    std::cout << "    [**]    \n";
    std::cout << " O--|  |--O \n";
    std::cout << "    |__|    \n";
    std::cout << "   _|  |_   \n";
}

void printDragon(){
    std::cout << "      _______         \n";
    std::cout << "  ^ ^ |               \n";
    std::cout << " {* *}--------        \n";
    std::cout << "  \\_/________=====>> \n";
    std::cout << "   //       \\\\     \n";
    
}

#endif  // MONSTERPRINT_H_
